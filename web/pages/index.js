import { useEffect, useState } from "react";
import { useRouter } from "next/router";
import { Card, Layout, Page, Spinner } from "@shopify/polaris";
import Head from "next/head";

import { userLoggedInFetch } from "@lib/fetch";

export default function Home() {
    const router = useRouter();
    const fetchFunction = userLoggedInFetch();

    const [loading, setLoading] = useState(router.query?.id ? true : false);

    useEffect(() => {
        if (router.query?.id) {
            convertToDraft();
        }
    }, []);

    const convertToDraft = async () => {
        try {
            const response = await fetchFunction(`/api/admin/convert`).then((res) => {
                if (!res) {
                    return null;
                }
                return res?.json()
            });

            console.log({ response })
            
        } catch (error) {
            console.warn(error);
        }
    }

    if (router.query?.id) {
        if (loading) {
            return (
                <Page>
                    <Layout>
                        <Layout.Section>
                            <Card
                                sectioned
                                title="Creating draft order..."
                            >
                                <Spinner />
                            </Card>
                        </Layout.Section>
                    </Layout>
                </Page>
            )
        }

        return (
            <Page>
                <Layout>
                    <Layout.Section>
                        <Card
                            sectioned
                            title="Redirecting to draft order..."
                        >
                            <Spinner />
                        </Card>
                    </Layout.Section>
                </Layout>
            </Page>
        )
    }

    return (
        <Page>
            <Head>
                <title>Outcast Shopify App</title>
                <meta
                    name="description"
                    content="Generated by create next app"
                />
                <link rel="icon" href="/favicon.ico" />
            </Head>

            <Layout>
                <Layout.Section>
                    <Card
                        sectioned
                        title="First, select an abandoned checkout"
                        //actions={[{ content: "Settings", url: "/settings"}]}
                    >
                        Navigate to Orders &rarr; Abandoned checkouts. Select an abandoned checkout and then click: More
                        actions &rarr; Convert to draft order.
                    </Card>
                </Layout.Section>
            </Layout>
        </Page>
    );
}
